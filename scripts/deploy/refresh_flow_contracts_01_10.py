def http_json(method: str, path: str, payload: dict | None = None) -> dict:
    body = None if payload is None else json.dumps(payload, ensure_ascii=False).encode("utf-8")
    req = request.Request(
        f"{BASE_URL}{path}",
        data=body,
        method=method,
        headers={"Content-Type": "application/json; charset=utf-8"},
    )
    with request.urlopen(req, timeout=30) as resp:
        return json.loads(resp.read().decode("utf-8"))


def contract(
    stance_name: str,
    objective: str,
    must_defend: str,
    must_challenge: str,
    evidence_standard: str,
    risk_bias: str,
    forbidden_overlap: str,
    output_schema: list[str],
) -> dict:
    return {
        "stance_name": stance_name,
        "objective": objective,
        "must_defend": must_defend,
        "must_challenge": must_challenge,
        "evidence_standard": evidence_standard,
        "risk_bias": risk_bias,
        "forbidden_overlap": forbidden_overlap,
        "output_schema": output_schema,
    }


def adjudication(decision_rule: str, rubric: list[str], sections: list[str]) -> dict:
    return {
        "decision_rule": decision_rule,
        "rubric": rubric,
        "required_output_sections": sections,
    }


