def normalize_edges_if_needed(flow_id: int, spec: dict) -> None:
    edges = spec.get("edges")
    if isinstance(edges, dict):
        spec["edges"] = [edges]


def main() -> int:
    BACKUP_DIR.mkdir(exist_ok=True)
    data = http_json("GET", "/flows")
    flows = sorted((flow for flow in data["flows"] if flow["id"] in TARGET_IDS), key=lambda item: item["id"])
    if {flow["id"] for flow in flows} != TARGET_IDS:
        raise RuntimeError("expected production flow ids 29-38")

    backup_path = BACKUP_DIR / "flows-01-10-refresh-input.json"
    backup_path.write_text(json.dumps(flows, ensure_ascii=False, indent=2), encoding="utf-8")

    updated = []
    for flow in flows:
        flow_id = flow["id"]
        spec = deepcopy(flow.get("flow_spec") or {})
        if not isinstance(spec, dict):
            spec = {}
        normalize_edges_if_needed(flow_id, spec)
        spec["role_contracts"] = CONTRACTS[flow_id]
        spec["adjudication"] = ADJUDICATION[flow_id]
        result = http_json("PUT", f"/flows/{flow_id}", {"flow_spec": spec})
        updated.append(result)

    for flow in updated:
        flow_id = flow["id"]
        contracts = (flow.get("flow_spec") or {}).get("role_contracts") or {}
        judge = (flow.get("flow_spec") or {}).get("adjudication") or {}
        expected_roles = set(flow["role_ids"])
        if set(contracts) != expected_roles:
            raise RuntimeError(f"flow {flow_id} contract roles mismatch: {set(contracts)} != {expected_roles}")
        if not judge.get("decision_rule") or not judge.get("rubric"):
            raise RuntimeError(f"flow {flow_id} missing adjudication")
        print(
            f"[ok] {flow_id} {flow['name']} contracts={len(contracts)} "
            f"rubric={len(judge.get('rubric') or [])}"
        )
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
